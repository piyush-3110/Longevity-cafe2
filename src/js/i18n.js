let currentLang = 'en';

export function getLang() {
  return currentLang;
}

export function t(key) {
  return translations[currentLang][key] ?? translations.en[key] ?? key;
}

export function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const value = translations[lang][el.dataset.i18n];
    if (value !== undefined) el.textContent = value;
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const value = translations[lang][el.dataset.i18nHtml];
    if (value !== undefined) el.innerHTML = value;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const value = translations[lang][el.dataset.i18nPlaceholder];
    if (value !== undefined) el.placeholder = value;
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
    const value = translations[lang][el.dataset.i18nAlt];
    if (value !== undefined) el.alt = value;
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    const value = translations[lang][el.dataset.i18nAriaLabel];
    if (value !== undefined) el.setAttribute('aria-label', value);
  });

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && translations[lang]['meta.description']) {
    metaDesc.content = translations[lang]['meta.description'];
  }

  if (translations[lang]['meta.title']) {
    document.title = translations[lang]['meta.title'];
  }

  const navToggle = document.querySelector('.nav__toggle');
  if (navToggle) {
    const isOpen = navToggle.getAttribute('aria-expanded') === 'true';
    navToggle.setAttribute('aria-label', t(isOpen ? 'nav.closeMenu' : 'nav.openMenu'));
  }

  document.querySelectorAll('.lang-toggle').forEach((group) => {
    group.setAttribute('aria-label', t('lang.label'));
  });

  document.querySelectorAll('.lang-toggle__btn').forEach((btn) => {
    const isActive = btn.dataset.lang === lang;
    btn.classList.toggle('is-active', isActive);
    btn.setAttribute('aria-pressed', String(isActive));
  });

  localStorage.setItem('lang', lang);
}

export function initI18n() {
  const saved = localStorage.getItem('lang');
  const browserLang = navigator.language?.startsWith('de') ? 'de' : 'en';
  setLanguage(saved && translations[saved] ? saved : browserLang);

  document.querySelectorAll('.lang-toggle__btn').forEach((btn) => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
}
